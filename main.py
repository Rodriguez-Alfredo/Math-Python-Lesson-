#!/bin/python3

print(50 + 50) #add
print(50 -50) #subtract
print(50 * 50) #multiply
print(50/50) #divide
print(50 + 50 - 50 * 50 / 50) #PEMDAS
print(50 ** 2) #exponents
print(50 % 6) #modulo - remainder
print(50 / 6) #division with remainder (or a float)
print(50 // 6) #no remainder (only integer)

